int	ft_atoi(const char *str)
{
	int	i;
	int	s;
	int	res;

	i = 0;
	res = 0;
	s = 1;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9' && str[i] != '\0')
	{
		res = ((str[i] - '0') + (res * 10));
		i++;
	}
	return (res * s);
}
